#include "3-calc.h"
/**
 * op_add - function performs addition.
 * @a: 1st int
 * @b: 2nd int
 * Return: an int
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function performs subtraction.
 * @a: 1st int
 * @b: 2nd int
 * Return: an int
 */

int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function performs multiplication
 * @a: 1st int
 * @b: 2nd int
 * Return: an int
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function performs division
 * @a: 1st int
 * @b: 2nd int
 * Return: an int
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function performs division
 * @a: 1st int
 * @b: 2nd int
 * Return: an int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
